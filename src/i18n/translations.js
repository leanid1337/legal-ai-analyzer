export const SUPPORTED_LOCALES = ['en', 'cs', 'uk', 'ru']

export function resolveInitialLocale() {
  try {
    const saved = localStorage.getItem('legal-ai-lang')
    if (saved && SUPPORTED_LOCALES.includes(saved)) return saved
  } catch {
    /* ignore */
  }
  const nav = (typeof navigator !== 'undefined' && navigator.language) || 'en'
  const low = nav.toLowerCase()
  if (low.startsWith('cs') || low.startsWith('cz')) return 'cs'
  if (low.startsWith('uk')) return 'uk'
  if (low.startsWith('ru')) return 'ru'
  return 'en'
}
